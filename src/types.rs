use std::{num::NonZeroU64, u8};

use crate::consts::*;
use deku::prelude::*;

#[derive(Debug, PartialEq, DekuRead, DekuWrite)]
#[deku(endian = "big")]
pub struct IKEHeader {
    initiator_spi: NonZeroU64,
    responder_spi: u64,
    next_payload: PayloadType,
    #[deku(bits = 4)]
    major_version: u8,
    #[deku(bits = 4)]
    minor_version: u8,
    exchange_type: ExchangeType,
    flags: Flags,
    message_id: u32,
    length: u32,
}

#[derive(Clone, Debug, PartialEq, DekuRead, DekuWrite)]
#[deku(endian = "big")]
pub struct PayloadHeader {
    next_payload: PayloadType,
    #[deku(bits = 1)]
    critical: bool,
    #[deku(bits = 7)]
    reserved: u8,
    payload_length: u16,
}

#[derive(Clone, Debug, PartialEq, DekuRead, DekuWrite)]
#[deku(endian = "big")]
pub struct Proposal {
    last_substructure: LastSubstructure,
    reserved: u8,
    proposal_length: u16,
    proposal_num: u8,
    protocol_id: ProtocolIdentifier,
    #[deku(update = "self.spi.len()")]
    spi_size: u8,
    #[deku(update = "self.transforms.len()")]
    num_transforms: u8,
    #[deku(count = "spi_size")]
    spi: Vec<u8>,
    #[deku(count = "num_transforms")]
    transforms: Vec<Transform>,
}

#[derive(Clone, Debug, PartialEq, DekuRead, DekuWrite)]
#[deku(endian = "endian", ctx = "endian: deku::ctx::Endian")]
pub struct Transform {
    last_substructure: LastSubstructure,
    reserved_0: u8,
    #[deku(update = "self.transform_attributes.len() + 8")]
    transform_length: u16,
    transform_type: TransformType,
    #[deku(count = "transform_length - 8")]
    transform_attributes: Vec<u8>,
}

#[derive(Debug, PartialEq, DekuRead, DekuWrite)]
#[deku(endian = "endian", ctx = "endian: deku::ctx::Endian")]
pub struct Flags {
    #[deku(bits = 1)]
    unused_0: bool,
    #[deku(bits = 1)]
    unused_1: bool,
    #[deku(bits = 1)]
    response: bool,
    #[deku(bits = 1)]
    version: bool,
    #[deku(bits = 1)]
    initiator: bool,
    #[deku(bits = 1)]
    unused_2: bool,
    #[deku(bits = 1)]
    unused_3: bool,
    #[deku(bits = 1)]
    unused_4: bool,
}

